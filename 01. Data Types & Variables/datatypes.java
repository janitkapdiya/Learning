public class datatypes {
  public static void main(String arg[]){
    byte bt = 8; //256 Characters Storing Capacity
    System.out.println(bt);

    char ch = 'A'; // In char we can store only single character
    System.out.println(ch);

    boolean bull = true; // In Boolean we can store True or False Statements 
    System.out.println(bull);

    float price = 10.2f; // In float we can store point values
    System.out.println(price);

    int integer = 1; // In Integer we can store. 
    System.out.println(integer);

    //Short, Long, & double are same as int.
  }  
}
